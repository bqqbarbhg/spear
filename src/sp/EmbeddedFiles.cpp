struct EmbeddedContentPackage : ContentPackage
{
	sf::HashMap<sf::StringBuf, sf::Slice<const uint8_t>> files;

	EmbeddedContentPackage()
	{
		files["sp://White.png"] = whitePng;
		files["sp://OpenSans-Ascii.ttf"] = openSansAsciiTtf;
	}

	virtual bool shouldTryToLoad(const sf::String &name) final
	{
		// Require sp:// prefix
		if (name.size < 5) return false;
		const char *d = name.data;
		if (d[0]!='s' || d[1]!='p' || d[2]!=':' || d[3]!='/' || d[4]!='/') return false;

		return files.find(name) != nullptr;
	}

	virtual bool startLoadingFile(ContentLoadHandle handle, const sf::String &name) final
	{
		auto it = files.find(name);
		sf_assert(it != nullptr);
		sf::Slice<const uint8_t> data = it->val;
		ContentFile::packageFileLoaded(handle, data.data, data.size, true);
		return true;
	}
};

ContentPackage *getEmbeddedContentPackage()
{
	static EmbeddedContentPackage package;
	return &package;
}

